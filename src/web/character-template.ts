export function getTemplateWithName(name: string): Record<string, string> {
  const template: Record<string, string> = {};
  
  for (const [filename, content] of Object.entries(characterTemplate)) {
    template[filename] = content.replace(/\[角色名\]/g, name);
  }
  
  return template;
}
